def show_bytecode(bytecode):
    """Show generated bytecode."""
    print("\nGenerated Bytecode:")
    
    if bytecode.main_function:
        print("  Main function:")
        for i, instruction in enumerate(bytecode.main_function.bytecode):
            print(f"    {i:3d}: {instruction}")
    
    if bytecode.functions:
        print("  Functions:")
        for name, func in bytecode.functions.items():
            print(f"    {name}:")
            for i, instruction in enumerate(func.bytecode):
                print(f"      {i:3d}: {instruction}")


def save_bytecode(bytecode, output_path: str):
    """Save bytecode to file using binary serialization."""
    try:
        # Use the bytecode generator's serialization
        from runa.compiler.bytecode_generator import BytecodeGenerator
        from runa.compiler.semantic_analyzer import SemanticAnalyzer
        
        # Create a temporary generator for serialization
        semantic_analyzer = SemanticAnalyzer()
        generator = BytecodeGenerator(semantic_analyzer)
        
        # Serialize to binary format
        binary_data = generator.serialize_bytecode(bytecode)
        
        # Save binary data
        with open(output_path, 'wb') as f:
            f.write(binary_data)
        
        print(f"✓ Bytecode saved to {output_path} ({len(binary_data)} bytes)")
        
    except Exception as e:
        print(f"Error saving bytecode: {e}")
        # Fallback to text representation
        try:
            with open(output_path, 'w') as f:
                f.write("RUNA_BYTECODE_V1\n")
                f.write(f"Functions: {len(bytecode.functions)}\n")
                f.write(f"Constants: {len(bytecode.constants)}\n")
                
                if bytecode.main_function:
                    f.write(f"Main function instructions: {len(bytecode.main_function.bytecode)}\n")
            print(f"✓ Bytecode saved as text to {output_path}")
        except Exception as e2:
            print(f"Error saving bytecode (fallback): {e2}")


