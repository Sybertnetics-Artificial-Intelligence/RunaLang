#!/usr/bin/env python3
"""
Runa Command Line Interface

A simple CLI for testing and demonstrating the Runa programming language compiler.
Supports compilation, execution, and performance monitoring.
"""

import sys
import argparse
import time
from pathlib import Path
from typing import Optional

from runa.compiler import Compiler


def main():
    """Main CLI entry point."""
    parser = argparse.ArgumentParser(
        description="Runa Programming Language Compiler",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog="""
Examples:
  runa_cli.py compile program.runa
  runa_cli.py compile -o program.bc program.runa
  runa_cli.py run program.runa
  runa_cli.py benchmark program.runa
        """
    )
    
    subparsers = parser.add_subparsers(dest='command', help='Available commands')
    
    # Compile command
    compile_parser = subparsers.add_parser('compile', help='Compile Runa source code')
    compile_parser.add_argument('input', help='Input Runa source file')
    compile_parser.add_argument('-o', '--output', help='Output bytecode file')
    compile_parser.add_argument('--show-ast', action='store_true', help='Show AST after parsing')
    compile_parser.add_argument('--show-bytecode', action='store_true', help='Show generated bytecode')
    compile_parser.add_argument('--show-metrics', action='store_true', help='Show performance metrics')
    
    # Run command
    run_parser = subparsers.add_parser('run', help='Compile and execute Runa source code')
    run_parser.add_argument('input', help='Input Runa source file')
    run_parser.add_argument('--show-metrics', action='store_true', help='Show performance metrics')
    
    # Benchmark command
    benchmark_parser = subparsers.add_parser('benchmark', help='Benchmark compilation performance')
    benchmark_parser.add_argument('input', help='Input Runa source file')
    benchmark_parser.add_argument('-n', '--iterations', type=int, default=100, help='Number of iterations')
    
    # Interactive command
    interactive_parser = subparsers.add_parser('interactive', help='Interactive Runa shell')
    interactive_parser.add_argument('--show-metrics', action='store_true', help='Show performance metrics')
    
    args = parser.parse_args()
    
    if not args.command:
        parser.print_help()
        return 1
    
    try:
        if args.command == 'compile':
            return compile_command(args)
        elif args.command == 'run':
            return run_command(args)
        elif args.command == 'benchmark':
            return benchmark_command(args)
        elif args.command == 'interactive':
            return interactive_command(args)
        else:
            print(f"Unknown command: {args.command}")
            return 1
            
    except KeyboardInterrupt:
        print("\nInterrupted by user")
        return 1
    except Exception as e:
        print(f"Error: {e}")
        return 1


def compile_command(args) -> int:
    """Handle compile command."""
    input_path = Path(args.input)
    
    if not input_path.exists():
        print(f"Error: Input file '{args.input}' does not exist")
        return 1
    
    # Read source code
    try:
        with open(input_path, 'r', encoding='utf-8') as f:
            source_code = f.read()
    except Exception as e:
        print(f"Error reading file: {e}")
        return 1
    
    # Compile
    print(f"Compiling {input_path}...")
    compiler = Compiler()
    
    try:
        bytecode = compiler.compile(source_code)
        print("✓ Compilation successful!")
        
        # Show metrics if requested
        if args.show_metrics:
            show_metrics(compiler)
        
        # Show AST if requested
        if args.show_ast:
            show_ast(compiler, source_code)
        
        # Show bytecode if requested
        if args.show_bytecode:
            show_bytecode(bytecode)
        
        # Save bytecode if output specified
        if args.output:
            save_bytecode(bytecode, args.output)
            print(f"Bytecode saved to {args.output}")
        
        return 0
        
    except Exception as e:
        print(f"✗ Compilation failed: {e}")
        return 1


