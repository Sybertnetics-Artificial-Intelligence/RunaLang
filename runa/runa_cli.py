def benchmark_command(args) -> int:
    """Handle benchmark command."""
    input_path = Path(args.input)
    
    if not input_path.exists():
        print(f"Error: Input file '{args.input}' does not exist")
        return 1
    
    # Read source code
    try:
        with open(input_path, 'r', encoding='utf-8') as f:
            source_code = f.read()
    except Exception as e:
        print(f"Error reading file: {e}")
        return 1
    
    # Benchmark
    print(f"Benchmarking {input_path} ({args.iterations} iterations)...")
    compiler = Compiler()
    
    times = []
    memory_usage = []
    
    for i in range(args.iterations):
        start_time = time.perf_counter()
        bytecode = compiler.compile(source_code)
        end_time = time.perf_counter()
        
        times.append((end_time - start_time) * 1000)
        
        # Get memory usage if available
        try:
            import psutil
            import os
            process = psutil.Process(os.getpid())
            memory_usage.append(process.memory_info().rss / (1024 * 1024))  # MB
        except ImportError:
            pass
        
        if (i + 1) % 10 == 0:
            print(f"  Completed {i + 1}/{args.iterations} iterations...")
    
    # Calculate statistics
    avg_time = sum(times) / len(times)
    min_time = min(times)
    max_time = max(times)
    
    print("\nBenchmark Results:")
    print(f"  Average compilation time: {avg_time:.2f}ms")
    print(f"  Minimum compilation time: {min_time:.2f}ms")
    print(f"  Maximum compilation time: {max_time:.2f}ms")
    print(f"  Performance target: <100ms")
    print(f"  Target met: {'✓' if avg_time < 100 else '✗'}")
    
    if memory_usage:
        avg_memory = sum(memory_usage) / len(memory_usage)
        print(f"  Average memory usage: {avg_memory:.2f}MB")
    
    return 0


def interactive_command(args) -> int:
    """Handle interactive command."""
    print("Runa Interactive Shell")
    print("Type 'exit' to quit, 'help' for help")
    print()
    
    compiler = Compiler()
    line_number = 1
    
    while True:
        try:
            # Get input
            prompt = f"runa:{line_number:03d}> "
            source_code = input(prompt).strip()
            
            if not source_code:
                continue
            
            if source_code.lower() in ['exit', 'quit']:
                break
            
            if source_code.lower() == 'help':
                show_help()
                continue
            
            # Compile and execute
            try:
                bytecode = compiler.compile(source_code)
                print("✓ Success")
                
                if args.show_metrics:
                    show_metrics(compiler)
                
            except Exception as e:
                print(f"✗ Error: {e}")
            
            line_number += 1
            
        except KeyboardInterrupt:
            print("\nUse 'exit' to quit")
        except EOFError:
            break
    
    print("Goodbye!")
    return 0


