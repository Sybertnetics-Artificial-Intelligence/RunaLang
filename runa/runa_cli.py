    """Show compilation performance metrics."""
    metrics = compiler.get_performance_metrics()
    
    print("\nPerformance Metrics:")
    print(f"  Total compilation time: {metrics['total_compilation_time_ms']:.2f}ms")
    print(f"  Target time: {metrics['target_time_ms']:.2f}ms")
    print(f"  Performance ratio: {metrics['performance_ratio']:.2f}x")
    
    if 'lexical_analysis_time_ms' in metrics:
        print(f"  Lexical analysis: {metrics['lexical_analysis_time_ms']:.2f}ms")
    if 'parsing_time_ms' in metrics:
        print(f"  Parsing: {metrics['parsing_time_ms']:.2f}ms")
    if 'semantic_analysis_time_ms' in metrics:
        print(f"  Semantic analysis: {metrics['semantic_analysis_time_ms']:.2f}ms")
    if 'bytecode_generation_time_ms' in metrics:
        print(f"  Bytecode generation: {metrics['bytecode_generation_time_ms']:.2f}ms")
    
